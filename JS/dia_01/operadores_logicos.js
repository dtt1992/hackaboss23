// Operadores logicos y Relaciones

// Operador de asignacion
//  a = b
const hola = "Hola mundo";
const numeroCinco = 5 ;
const edadJuan = 18;
const mayoriaEdad = 18;
let saludar;
const n = 0;
const afirmacion = "si"

// Igualdad
//  a == b
hola == "Hola mundo"
numeroCinco == "5";
numeroCinco == 5;

// Igualdad de valores y tipos
// a === b
numeroCinco === "5";
numeroCinco === 5;

// Mayor que
// a > b
numeroCinco > 2;
numeroCinco > "2";

// Mayor o igual que
// a >= b
numeroCinco >= 2;
numeroCinco >= "2";
edadJuan >= mayoriaEdad

// Menor que
// a < b
numeroCinco < 2;
numeroCinco < "2";

// Menor o igual que
// a <= b
numeroCinco <= 2;
numeroCinco <= "2";
edadJuan <= 17;

// Condicion AND
//  a && b
// Si la primera es falsa, no ejecutara la segunda, va en orden
edadJuan >= 18 && mayoriaEdad === 18;

// Condicion OR
//  a || b
edadJuan > 18 || mayoriaEdad === 18;

// Distinto de
// a != b
edadJuan != mayoriaEdad;

// Negacion
// !a
saludar == undefined;
saludar != undefined && saludar != null && saludar != false
!saludar

n == undefined;
n == null;
n == false;